public class FullbrightMod extends Module {

	public FullbrightMod() {
		super(new ModData("Fullbright", 0, new Color(66, 174, 212)),
				ModType.RENDER);
	}
	
	@Override
	public void onDisable() {
		Mineman.getMinecraft().gameSettings.gammaSetting = 1.0f;
	}
	
	@EventListener
	public void onTick(EventPlayerUpdate event){
		Mineman.getMinecraft().gameSettings.gammaSetting = 10.0f;
	}
	

}
