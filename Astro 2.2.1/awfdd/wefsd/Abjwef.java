package awfdd.wefsd;

import awfdd.wefsd.awdsaef.awdfsgjjj.awdsafe.awfdsserg.Suge;
import awfdd.wefsd.awdsaef.awdfsgjjj.awdsafe.awfdsserg.Zabef;
import awfdd.wefsd.awdsaef.awdfsgjjj.awdsafe.awfdsserg.rtdgf.HEfbhs;

/**
 * Any event bus that can be copied implements this interface
 * 
 * @author TCB
 *
 */
public interface Abjwef {
	/**
	 * Returns a new instance of this {@link Suge} with the same
	 * properties.
	 * Used in {@link Zabef} to create copies of
	 * the given bus.
	 * @return {@link HEfbhs}
	 */
	public HEfbhs copyBus();
}
